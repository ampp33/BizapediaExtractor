package org.malibu.msu.bizapedia;

public class BizapediaExtractorException extends Exception {

	private static final long serialVersionUID = 1L;

	public BizapediaExtractorException() {
		super();
	}

	public BizapediaExtractorException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public BizapediaExtractorException(String message, Throwable cause) {
		super(message, cause);
	}

	public BizapediaExtractorException(String message) {
		super(message);
	}

	public BizapediaExtractorException(Throwable cause) {
		super(cause);
	}

}
